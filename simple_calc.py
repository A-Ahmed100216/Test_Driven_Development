# This file will have our functional code
class SimpleCalc:

    def add(self,num1,num2):
        return num1 + num2


    def subtract(self,num1,num2):
        return num1-num2
        # pass

    def multiply(self,num1,num2):
        return num1*num2
        # pass

    def divide(self,num1,num2):
        return num1/num2
        # pass